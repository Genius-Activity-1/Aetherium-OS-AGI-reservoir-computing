# 5. MODÈLE AGI COMPLET (Losange Cognitif Tensoriel)
# ============================================================
class AetheriumTensorWorldModel(nn.Module):
    def __init__(self, cfg: AetheriumTensorConfig):
        super().__init__()
        self.cfg = cfg
        self.fluid_cell = RelativisticFluidCell(cfg)

        # Encodage perceptif elliptique (anisotrope)
        # On utilise des kernels rectangulaires pour forcer la détection de profils elliptiques
        self.perceive = nn.Conv2d(1, cfg.dim_tensor, kernel_size=(5, 3), padding=(2, 1))

        # Mapping initial
        self.to_phi = nn.Linear(cfg.dim_tensor, 1)
        self.to_psi = nn.Linear(cfg.dim_tensor, cfg.dim_tensor * 2)
        self.to_omega = nn.Linear(cfg.dim_tensor, cfg.dim_tensor)

    def forward(self, x: torch.Tensor, steps: int = 5):
        # x shape: (Batch, Channels, H, W)
        B = x.shape[0]

        # Perception elliptique (moyenne globale spatiale pour simplifier)
        feat = F.gelu(self.perceive(x)).mean(dim=[2, 3]) # (B, dim_tensor)

        # Initialisation du Losange Cognitif
        phi = self.to_phi(feat)
        psi_flat = self.to_psi(feat)
        psi = psi_flat.view(B, self.cfg.dim_tensor, 2)
        omega = self.to_omega(feat)

        phi_hist, psi_hist, omega_hist = [phi], [psi], [omega]
        coherences = []

        # Évolution temporelle (Écoulement du fluide)
        for _ in range(steps):
            phi, psi, omega, C = self.fluid_cell(phi, psi, omega)

            phi_hist.append(phi)
            psi_hist.append(psi)
            omega_hist.append(omega)
            coherences.append(C)

        return (
            torch.stack(phi_hist, dim=1),
            torch.stack(psi_hist, dim=1),
            torch.stack(omega_hist, dim=1),
            torch.stack(coherences, dim=1)
        )

