def aetherium_agi_loss(
    model_output: Tuple,
    target_frames: torch.Tensor,
    cfg: AetheriumAGIConfig
):
    predictions, phys_observables, psi_history = model_output

    # 1. Perte de prédiction (World Model standard)
    loss_rec = F.mse_loss(predictions, target_frames)

    # 2. Perte Topologique & Stabilité (Le cœur de l'Aetherium)
    # Applique le certificat de Collatz pour garantir l'absence de blow-up
    loss_collatz = collatz_lyapunov_loss(
        psi_history,
        k_blocks=cfg.k_blocks_collatz,
        epsilon=cfg.collatz_epsilon
    )

    # 3. (Optionnel) Ajout du Gap de Riemann Spectral
    # loss_riemann = ... (pénalise les énergies de Psi qui ne respectent pas log(p))

    loss_total = loss_rec + 0.1 * loss_collatz

    return loss_total, loss_rec, loss_collatz

