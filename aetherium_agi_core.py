# 1. ENCODEUR SENSORIEL (Perception)
# ============================================================

class SensoryEncoder(nn.Module):
    """Projette les stimuli externes dans le fluide Aetherium."""
    def __init__(self, cfg: AetheriumAGIConfig):
        super().__init__()
        # Extraction de caractéristiques
        self.net = nn.Sequential(
            nn.Conv2d(cfg.in_channels, cfg.base_channels, 4, 2, 1),
            nn.GELU(),
            nn.Conv2d(cfg.base_channels, cfg.base_channels * 2, 4, 2, 1),
            nn.GELU(),
            nn.Conv2d(cfg.base_channels * 2, cfg.base_channels * 4, 4, 2, 1),
            nn.GELU(),
        )

        # Projection vers les 3 composantes du Losange Cognitif
        flat_size = (cfg.spatial_size // 8) ** 2 * (cfg.base_channels * 4)

        self.to_phi = nn.Linear(flat_size, cfg.dim_phi)
        # Psi a besoin de 2 canaux par dimension (Densité et Phase)
        self.to_psi_density = nn.Linear(flat_size, cfg.dim_psi)
        self.to_psi_phase = nn.Linear(flat_size, cfg.dim_psi)
        self.to_omega = nn.Linear(flat_size, cfg.dim_omega)

    def forward(self, x: torch.Tensor):
        B = x.shape[0]
        feat = self.net(x).view(B, -1)

        phi = self.to_phi(feat)

        # Psi = sqrt(density) * exp(i * phase)
        psi_density = F.softplus(self.to_psi_density(feat)) # Densité toujours positive
        psi_phase = torch.pi * torch.tanh(self.to_psi_phase(feat)) # Phase entre -pi et pi

        # Représentation complexe de Psi (Partie Réelle, Partie Imaginaire)
        psi_real = torch.sqrt(psi_density) * torch.cos(psi_phase)
        psi_imag = torch.sqrt(psi_density) * torch.sin(psi_phase)
        psi = torch.stack([psi_real, psi_imag], dim=-1) # (B, dim_psi, 2)

        omega = self.to_omega(feat)

        return phi, psi, omega

