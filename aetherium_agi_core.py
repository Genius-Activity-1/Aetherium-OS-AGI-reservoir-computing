class PhysicalCognitiveHead(nn.Module):
    """
    Traduit l'état fluide latent en prédictions observables (images, actions, ou
    signatures physiques comme Delta_Phi_GW ou Cohérence C).
    """
    def __init__(self, cfg: AetheriumAGIConfig):
        super().__init__()
        flat_size = cfg.dim_phi + cfg.dim_psi * 2 + cfg.dim_omega

        # Reconstruction Visuelle (Imagination)
        self.decoder = nn.Sequential(
            nn.Linear(flat_size, (cfg.spatial_size // 8) ** 2 * (cfg.base_channels * 4)),
            nn.GELU(),
            # ... Unflatten ... (omitted for brevity, assume inverse of encoder)
        )
        self.to_image = nn.Linear((cfg.spatial_size // 8) ** 2 * (cfg.base_channels * 4), cfg.in_channels * cfg.spatial_size * cfg.spatial_size)

        # Observables Physiques du Modèle Aetherium
        self.pred_coherence_c = nn.Linear(cfg.dim_psi * 2, 1) # Cohérence de phase
        self.pred_delta_phi_gw = nn.Linear(cfg.dim_phi + cfg.dim_psi * 2, 1) # Lensing GW

    def forward(self, phi, psi, omega):
        B = phi.shape[0]
        psi_flat = psi.view(B, -1)
        state_cat = torch.cat([phi, psi_flat, omega], dim=-1)

        # Décodage Image
        feat = self.decoder(state_cat)
        img_pred = self.to_image(feat).view(B, 3, 64, 64) # Hardcoded for 64x64 example

        # Décodage Physique
        c_pred = torch.sigmoid(self.pred_coherence_c(psi_flat)) # Entre 0 et 1
        gw_pred = self.pred_delta_phi_gw(torch.cat([phi, psi_flat], dim=-1))

        return img_pred, c_pred, gw_pred

