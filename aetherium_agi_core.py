class AetheriumAGIWorldModel(nn.Module):
    def __init__(self, cfg: AetheriumAGIConfig):
        super().__init__()
        self.cfg = cfg
        self.encoder = SensoryEncoder(cfg)
        self.fluid_core = FluidCognitiveCell(cfg)
        self.head = PhysicalCognitiveHead(cfg)

    def forward(self, x_seq: torch.Tensor, steps_to_predict: int = 1):
        """
        x_seq: (Batch, Time, Channels, Height, Width)
        """
        B, T_in, C, H, W = x_seq.shape

        # 1. Encodage de la séquence initiale
        phi_t, psi_t, omega_t = None, None, None
        psi_history = []

        for t in range(T_in):
            p, ps, o = self.encoder(x_seq[:, t])
            # Accumulation / Mise à jour récurrente (simplifiée ici)
            if phi_t is None:
                phi_t, psi_t, omega_t = p, ps, o
            else:
                # Évolution fluide incluant la nouvelle perception
                phi_t = phi_t + p * 0.1
                psi_t = psi_t + ps * 0.1
                omega_t = omega_t + o * 0.1
                phi_t, psi_t, omega_t = self.fluid_core(phi_t, psi_t, omega_t)

            psi_history.append(psi_t)

        # 2. Prédiction dans le futur (Imagination / Planification)
        predictions = []
        phys_observables = []

        for step in range(steps_to_predict):
            phi_t, psi_t, omega_t = self.fluid_core(phi_t, psi_t, omega_t)
            psi_history.append(psi_t)

            img_pred, c_pred, gw_pred = self.head(phi_t, psi_t, omega_t)
            predictions.append(img_pred)
            phys_observables.append((c_pred, gw_pred))

        psi_history = torch.stack(psi_history, dim=1) # (B, T_total, Dim, 2)
        predictions = torch.stack(predictions, dim=1) # (B, T_pred, C, H, W)

        return predictions, phys_observables, psi_history

