class DoubleVortexOrbitDataset(Dataset):
    """
    Génère deux masses (baryons/vortex) orbitant l'une autour de l'autre.
    Cela oblige l'IA à apprendre le moment cinétique (Omega) et l'attraction mutuelle.
    """
    def __init__(self, num_samples=100, seq_len=12, size=32):
        self.num_samples = num_samples
        self.seq_len = seq_len
        self.size = size

    def __len__(self): return self.num_samples

    def __getitem__(self, idx):
        frames = torch.zeros(self.seq_len, 1, self.size, self.size)
        # Centre de masse
        cx, cy = self.size / 2, self.size / 2
        # Rayon de l'orbite et vitesse angulaire (aléatoire par vidéo)
        radius = np.random.uniform(5, 10)
        omega = np.random.uniform(0.2, 0.5)
        phase_init = np.random.uniform(0, 2 * math.pi)

        for t in range(self.seq_len):
            # Position du Vortex 1
            x1 = cx + radius * math.cos(omega * t + phase_init)
            y1 = cy + radius * math.sin(omega * t + phase_init)
            # Position du Vortex 2 (opposé)
            x2 = cx + radius * math.cos(omega * t + phase_init + math.pi)
            y2 = cy + radius * math.sin(omega * t + phase_init + math.pi)

            # Dessin des vortex (flous pour imiter des densités)
            ix1, iy1 = int(np.clip(x1, 1, self.size-2)), int(np.clip(y1, 1, self.size-2))
            ix2, iy2 = int(np.clip(x2, 1, self.size-2)), int(np.clip(y2, 1, self.size-2))

            frames[t, 0, ix1-1:ix1+2, iy1-1:iy1+2] = 0.8
            frames[t, 0, ix2-1:ix2+2, iy2-1:iy2+2] = 0.8
            # Cœur plus dense
            frames[t, 0, ix1, iy1] = 1.0
            frames[t, 0, ix2, iy2] = 1.0

        return frames

