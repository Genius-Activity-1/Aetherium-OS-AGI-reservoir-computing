class RelativisticFluidCell(nn.Module):
    def __init__(self, cfg: AetheriumComplexConfig):
        super().__init__()
        self.cfg = cfg
        input_dim = 1 + cfg.dim_tensor * 3

        self.evolve_psi = nn.Sequential(
            nn.Linear(input_dim, cfg.dim_tensor * 2), nn.LayerNorm(cfg.dim_tensor * 2), nn.GELU()
        )
        self.evolve_omega = nn.Sequential(
            nn.Linear(input_dim, cfg.dim_tensor), nn.LayerNorm(cfg.dim_tensor), nn.Tanh()
        )

    def forward(self, phi, psi, omega):
        B = phi.shape[0]
        psi_flat = psi.view(B, -1)
        state_cat = torch.cat([phi, psi_flat, omega], dim=-1)

        # Évolution Quantique
        next_psi = (psi_flat + self.evolve_psi(state_cat)).view(B, self.cfg.dim_tensor, 2)

        # Dynamique Topologique (Vorticité)
        delta_omega = self.evolve_omega(state_cat)
        next_omega = omega + delta_omega

        # Équilibre de Bianchi
        real_curr, imag_curr = psi[..., 0], psi[..., 1]
        real_next, imag_next = next_psi[..., 0], next_psi[..., 1]

        density_current = (real_curr**2 + imag_curr**2).mean(dim=-1, keepdim=True)
        density_next = (real_next**2 + imag_next**2).mean(dim=-1, keepdim=True)

        phase_curr = torch.atan2(imag_curr, real_curr)
        C_curr = torch.exp(-torch.var(phase_curr, dim=-1, keepdim=True))
        beta_eff = self.cfg.beta_0 * (1 + self.cfg.k_coupling * C_curr)

        delta_phi = - (beta_eff * (density_next - density_current)) - (0.1 * delta_omega.mean(dim=-1, keepdim=True))
        next_phi = phi + delta_phi

        return next_phi, next_psi, next_omega

# (L'encodeur et le décodeur restent identiques à la version précédente pour condenser le code)
class SensoryEncoder(nn.Module):
    def __init__(self, cfg):
        super().__init__()
        self.net = nn.Sequential(
            nn.Conv2d(cfg.in_channels, cfg.base_channels, kernel_size=(5, 3), padding=(2, 1)), nn.GELU(),
            nn.Conv2d(cfg.base_channels, cfg.base_channels * 2, 4, 2, 1), nn.GELU(),
            nn.Conv2d(cfg.base_channels * 2, cfg.base_channels * 2, 4, 2, 1), nn.GELU(),
        )
        self.flat_size = (cfg.spatial_size // 4) ** 2 * (cfg.base_channels * 2)
        self.to_phi = nn.Linear(self.flat_size, 1)
        self.to_psi = nn.Linear(self.flat_size, cfg.dim_tensor * 2)
        self.to_omega = nn.Linear(self.flat_size, cfg.dim_tensor)

    def forward(self, x):
        feat = self.net(x).view(x.shape[0], -1)
        return self.to_phi(feat), self.to_psi(feat), self.to_omega(feat)

class CognitiveDecoder(nn.Module):
    def __init__(self, cfg):
        super().__init__()
        self.cfg = cfg
        self.flat_size = (cfg.spatial_size // 4) ** 2 * (cfg.base_channels * 2)
        self.to_feat = nn.Sequential(nn.Linear(1 + cfg.dim_tensor * 3, self.flat_size), nn.GELU())
        self.net = nn.Sequential(
            nn.ConvTranspose2d(cfg.base_channels * 2, cfg.base_channels, 4, 2, 1), nn.GELU(),
            nn.ConvTranspose2d(cfg.base_channels, cfg.in_channels, 4, 2, 1), nn.Sigmoid()
        )

    def forward(self, phi, psi, omega):
        B = phi.shape[0]
        state_cat = torch.cat([phi, psi.view(B, -1), omega], dim=-1)
        feat = self.to_feat(state_cat).view(B, self.cfg.base_channels * 2, self.cfg.spatial_size // 4, self.cfg.spatial_size // 4)
        return self.net(feat)

class AetheriumAGI(nn.Module):
    def __init__(self, cfg: AetheriumComplexConfig):
        super().__init__()
        self.cfg = cfg
        self.encoder = SensoryEncoder(cfg)
        self.fluid_cell = RelativisticFluidCell(cfg)
        self.decoder = CognitiveDecoder(cfg)
        self.observer = GWLensingObserver(cfg) # Ajout de l'observateur métrique

    def forward(self, x_seq: torch.Tensor, steps_to_predict: int):
        B, T_in, C, H, W = x_seq.shape
        phi, psi, omega = None, None, None
        phi_h, psi_h, omega_h, preds = [], [], [], []

        # Burn-in
        for t in range(T_in):
            p, ps_flat, o = self.encoder(x_seq[:, t])
            ps = ps_flat.view(B, self.cfg.dim_tensor, 2)
            if phi is None: phi, psi, omega = p, ps, o
            else:
                phi, psi, omega = phi + p*0.1, psi + ps*0.1, omega + o*0.1
                phi, psi, omega = self.fluid_cell(phi, psi, omega)
            phi_h.append(phi); psi_h.append(psi); omega_h.append(omega)

        # Imagination
        for step in range(steps_to_predict):
            phi, psi, omega = self.fluid_cell(phi, psi, omega)
            preds.append(self.decoder(phi, psi, omega))
            phi_h.append(phi); psi_h.append(psi); omega_h.append(omega)

        psi_history = torch.stack(psi_h, dim=1)

        # Extraction des Métriques Falsifiables
        C_metric, beta_eff_metric, delta_phi_gw = self.observer(psi_history)

        return (torch.stack(preds, dim=1), torch.stack(phi_h, dim=1),
                psi_history, torch.stack(omega_h, dim=1),
                C_metric, beta_eff_metric, delta_phi_gw)

